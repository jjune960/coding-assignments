package programmers._42746;

class Solution {
    public String solution(int[] numbers) {
        String answer = "";

        String[] numStrs = new String[numbers.length]; // 문자열 변환하기 위한 배열
        for (int i = 0; i < numbers.length; i++) {
            numStrs[i] = String.valueOf(numbers[i]);
        }
        
        

        return answer;
    }
}
